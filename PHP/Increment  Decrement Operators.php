<?php 

    /*
        Increment / Decrement Operators

        [ ++$Var ] Pre Increment 

        [ $Var++ ] Post Increment 

        [ --$Var ] Pre Decrement 

        [ $Var-- ] Post Decrement 
    
    */


$var1 = 5; // Pre Increment 

echo ++$var1 . "<br/>";
echo ++$var1 . "<br/>";

$var2 = 10; // Post Increment 

echo $var2++ . '<br/>';
echo $var2++ . '<br/>';

$var3 = 15; // Pre Decrement 

echo --$var3 . '<br/>';
echo --$var3 . '<br/>';

$var4 = 20; // Post Decrement

echo $var4-- . '<br/>';
echo $var4-- . '<br/>';

