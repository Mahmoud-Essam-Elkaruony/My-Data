/* Loop - For */

/*
    For Loop Syntax 

    for ( Initialization; Condition; Final Expression ) {

        // Statement
    }

    Initialization: Executed Befor The Loop once.
    Condition: Define The Condition To Run The Loop.
    Final Exepression: Executed Everytime The Code Run.

*/

var Friends = ["Amr", "Hmade", "Hussin", "Mosfe", "Mohab", "Ahade"];

var i;

for (i = 0; i < Friends.length; i += 1 ) { // i = i + 1 

    console.log(Friends[i]);

}

var r;

for (r = 100; r > 1 ; r-= 1) {

    console.log(r);
}

var c;

for (c = 1; c < 20; c += 1 ) {

    console.log(c);
}

































